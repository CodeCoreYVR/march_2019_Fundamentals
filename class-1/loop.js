/**
 * While is meant  to control
 * the flow of your program
 */

let index = 0;

// the expression index < 5
// will determine if the body
// of the while loop will
// get executed
while( index < 5) {
  console.log(`the value of index is ${index}`);
  index++;
}

/**
 * FOR LOOP
 * 
 * The equivalent for statment of the previous
 * while statement is as follows
 */
for ( let index = 0; index < 5; index++ ) {
  console.log(`the value of index is ${index}`);
}



